/**
 * calculator.spec.ts — calculator.ts の単体テスト
 *
 * テスト設計技法:
 * - 正常系: 正しい入力で期待通りの結果が返ること
 * - 異常系: 不正な入力で適切なエラーが発生すること
 * - 境界値: 0, 負の数, 小数点 などの境界付近の値
 */

import { describe, it, expect } from 'vitest'
import { add, subtract, multiply, divide, isPositiveInteger, sum, average } from '@/utils/calculator'

// ============================================================
// add（足し算）
// ============================================================
describe('add', () => {
  // 正常系
  it('2つの正の数を足せる', () => {
    expect(add(1, 2)).toBe(3)
  })

  it('負の数同士を足せる', () => {
    expect(add(-1, -2)).toBe(-3)
  })

  it('正の数と負の数を足せる', () => {
    expect(add(5, -3)).toBe(2)
  })

  // 境界値
  it('0を足すと元の数のまま', () => {
    expect(add(5, 0)).toBe(5)
  })

  it('小数を扱える', () => {
    expect(add(0.1, 0.2)).toBeCloseTo(0.3)
  })
})

// ============================================================
// subtract（引き算）
// ============================================================
describe('subtract', () => {
  it('正の数の引き算ができる', () => {
    expect(subtract(5, 3)).toBe(2)
  })

  it('結果が負の数になるケース', () => {
    expect(subtract(3, 5)).toBe(-2)
  })

  it('同じ数を引くと0になる', () => {
    expect(subtract(7, 7)).toBe(0)
  })
})

// ============================================================
// multiply（掛け算）
// ============================================================
describe('multiply', () => {
  it('正の数同士の掛け算ができる', () => {
    expect(multiply(3, 4)).toBe(12)
  })

  it('負の数と正の数を掛けると負の数になる', () => {
    expect(multiply(-3, 4)).toBe(-12)
  })

  it('0を掛けると0になる', () => {
    expect(multiply(100, 0)).toBe(0)
  })
})

// ============================================================
// divide（割り算）
// ============================================================
describe('divide', () => {
  // 正常系
  it('正の数の割り算ができる', () => {
    expect(divide(10, 2)).toBe(5)
  })

  it('割り切れない場合は小数になる', () => {
    expect(divide(10, 3)).toBeCloseTo(3.333, 2)
  })

  // 異常系
  it('0で割るとエラーが発生する', () => {
    expect(() => divide(10, 0)).toThrowError('0で割ることはできません')
  })
})

// ============================================================
// isPositiveInteger（正の整数判定）
// ============================================================
describe('isPositiveInteger', () => {
  // 正常系
  it('正の整数でtrueを返す', () => {
    expect(isPositiveInteger(1)).toBe(true)
    expect(isPositiveInteger(100)).toBe(true)
  })

  // 異常系・境界値
  it('0はfalseを返す', () => {
    expect(isPositiveInteger(0)).toBe(false)
  })

  it('負の整数はfalseを返す', () => {
    expect(isPositiveInteger(-1)).toBe(false)
  })

  it('小数はfalseを返す', () => {
    expect(isPositiveInteger(1.5)).toBe(false)
  })
})

// ============================================================
// sum（配列の合計）
// ============================================================
describe('sum', () => {
  it('配列の合計を計算できる', () => {
    expect(sum([1, 2, 3, 4, 5])).toBe(15)
  })

  it('要素が1つの配列を扱える', () => {
    expect(sum([42])).toBe(42)
  })

  it('負の数を含む配列を扱える', () => {
    expect(sum([-1, 1, -2, 2])).toBe(0)
  })

  it('空の配列でエラーが発生する', () => {
    expect(() => sum([])).toThrowError('空の配列は許可されていません')
  })
})

// ============================================================
// average（配列の平均）
// ============================================================
describe('average', () => {
  it('配列の平均を計算できる', () => {
    expect(average([10, 20, 30])).toBe(20)
  })

  it('小数の平均を正しく計算できる', () => {
    expect(average([1, 2])).toBe(1.5)
  })

  it('空の配列でエラーが発生する', () => {
    expect(() => average([])).toThrowError('空の配列は許可されていません')
  })
})
